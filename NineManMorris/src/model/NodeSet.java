package model;

// always consists of 3 Nodes, we have 20 nodeSets in total
public class NodeSet {
	public Node[] nodes = new Node[3];
	
	public NodeSet(Node first) {
		nodes[0] = first;
	}
	
	public void setSecond(Node node) {
		nodes[1] = node;
	}
	
	public void setThird(Node node) {
		nodes[2] = node;
	}
	
}
